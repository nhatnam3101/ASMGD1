package com.example.asmgd1.Model;

public class Khoahoc {
    public String Makh;
    public String Tenkh;
    public String startt;
    public String endt;

    public Khoahoc() {
    }

    public Khoahoc(String makh, String tenkh, String startt, String endt) {
        Makh = makh;
        Tenkh = tenkh;
        this.startt = startt;
        this.endt = endt;
    }
}
